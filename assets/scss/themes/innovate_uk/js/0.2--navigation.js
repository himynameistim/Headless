// A.1. NAVIGATION ----------------------------------------------------------------------------------------------------

(function () {
  var
    menu = document.querySelector('.o-nav'),
    hamburgerlink = document.querySelector('.o-nav .a-nav-toggle');

  hamburgerlink.addEventListener('click', function (e) {
    e.preventDefault();
    menu.classList.toggle('_active');
    hamburgerlink.classList.toggle('_active');
  });

})();

